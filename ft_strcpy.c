char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*sr;

	sr = (char*)src;
	i = 0;
	while (sr[i] != '\0')
	{
		dst[i] = sr[i];
		i++;
	}
	return (dst);
}
